import java.io.*;
import java.util.*;

public class Main {
    public static float sum(float n, float m){
        return n+m;
    }

    public static float sub(float n, float m){
        return n-m;
    }

    public static float mul(float n, float m){
        return n*m;
    }

    public static float div(float n, float m){
        return n/m;
    }
}